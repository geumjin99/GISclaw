def encode_image_b64(path):
    with open(path, "rb") as f:
        return base64.b64encode(f.read()).decode("utf-8")

def evaluate_images_vision(gold_img_path, agent_img_path, task_instruction):
    """Vision LLM (default Gemini-2.5-Flash) compares gold vs agent maps from GIS-expert view."""
    client, model = _vision_client()

    gold_b64 = encode_image_b64(gold_img_path)
    agent_b64 = encode_image_b64(agent_img_path)

    user_content = [
        {"type": "text", "text": f"## Task Description\n{task_instruction}\n\n"
                                  f"Below are two map outputs. Image 1 is the Gold Standard, Image 2 is the Agent Output.\n"
                                  f"Evaluate the Agent Output. Return JSON only."},
        {"type": "text", "text": "### Image 1: Gold Standard"},
        {"type": "image_url", "image_url": {"url": f"data:image/png;base64,{gold_b64}"}},
        {"type": "text", "text": "### Image 2: Agent Output"},
        {"type": "image_url", "image_url": {"url": f"data:image/png;base64,{agent_b64}"}},
    ]

    resp = client.chat.completions.create(
        model=model,
        messages=[
            {"role": "system", "content": VISION_SYSTEM_PROMPT},
            {"role": "user", "content": user_content},
        ],
        temperature=0.0,
        max_tokens=2048,
    )

    raw = resp.choices[0].message.content.strip()
    json_match = re.search(r'\{[\s\S]*\}', raw)
    if json_match:
        return json.loads(json_match.group())
    raise ValueError(f" JSON: {raw[:200]}")

def evaluate_image_standalone(agent_img_path, task_instruction):
    """Vision LLM evaluates agent image when no gold image is available."""
    client, model = _vision_client()
    agent_b64 = encode_image_b64(agent_img_path)

    user_content = [
        {"type": "text", "text": f"## Task Description\n{task_instruction}\n\n"
                                  f"Below is a map output generated by an AI agent for the above task.\n"
                                  f"There is no gold standard available. Evaluate this output on its own merit.\n"
                                  f"Return JSON only."},
        {"type": "image_url", "image_url": {"url": f"data:image/png;base64,{agent_b64}"}},
    ]

    resp = client.chat.completions.create(
        model=model,
        messages=[
            {"role": "system", "content": VISION_SYSTEM_PROMPT.replace(
                "You will receive TWO images:", "You will receive ONE image (no gold standard available):"
            ).replace("1. **Gold Standard**", "1. (Not available)")},
            {"role": "user", "content": user_content},
        ],
        temperature=0.0,
        max_tokens=2048,
    )
    raw = resp.choices[0].message.content.strip()
    json_match = re.search(r'\{[\s\S]*\}', raw)
    if json_match:
        return json.loads(json_match.group())
    raise ValueError(f" JSON: {raw[:200]}")

