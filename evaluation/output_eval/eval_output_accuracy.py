def get_output_files(directory):
    """"""
    if not os.path.isdir(directory):
        return []
    return [f for f in os.listdir(directory) if f not in SKIP_FILES and not f.startswith('.') and not f.startswith('__')]

def match_gold_file(agent_file, gold_files):
    """ gold """
    if agent_file in gold_files:
        return agent_file
    agent_ext = agent_file.rsplit('.', 1)[-1].lower() if '.' in agent_file else ''
    same_ext = [f for f in gold_files if f.rsplit('.', 1)[-1].lower() == agent_ext]
    return same_ext[0] if len(same_ext) == 1 else None

def evaluate_single(task_id, model_label, model_dir, gold_dir, task_instruction, skip_vision=False):
    """ task × model """
    result = {
        "task": f"T{task_id:02d}",
        "model": model_label,
        "eval_time": datetime.now().isoformat(),
        "gold_source": gold_dir,
        "outputs": [],
        "overall_score": 0.0,
        "task_success": False,
    }

    agent_files = get_output_files(model_dir)
    gold_files = get_output_files(gold_dir) if os.path.isdir(gold_dir) else []

    if not agent_files:
        result["overall_score"] = 0.0
        result["task_success"] = False
        return result

    scores = []

    for af in agent_files:
        agent_path = os.path.join(model_dir, af)
        ext = af.rsplit('.', 1)[-1].lower() if '.' in af else ''
        file_size = os.path.getsize(agent_path) if os.path.exists(agent_path) else 0

        entry = {
            "filename": af, "type": ext, "exists": True,
            "size_bytes": file_size, "metrics": {}
        }

        # gold 
        gold_match = match_gold_file(af, gold_files)
        gold_path = os.path.join(gold_dir, gold_match) if gold_match else None
        entry["gold_filename"] = gold_match

        if ext == 'png' and not skip_vision:
            try:
                if gold_path and os.path.exists(gold_path):
                    vision_result = evaluate_images_vision(gold_path, agent_path, task_instruction)
                else:
                    vision_result = evaluate_image_standalone(agent_path, task_instruction)

                entry["metrics"] = vision_result
                # 5 0-1
                dim_scores = []
                for dim in ["task_completion", "spatial_accuracy", "visual_readability",
                            "cartographic_quality", "data_integrity"]:
                    if dim in vision_result and isinstance(vision_result[dim], dict):
                        dim_scores.append(vision_result[dim].get("score", 0) / 5.0)
                entry["metrics"]["score"] = round(np.mean(dim_scores), 2) if dim_scores else 0.0
                scores.append(entry["metrics"]["score"])
            except Exception as e:
                entry["metrics"] = {"error": str(e), "score": 0.0}
                scores.append(0.0)

        elif ext == 'tif' or ext == 'tiff':
            if gold_path and os.path.exists(gold_path):
                entry["metrics"] = evaluate_raster(gold_path, agent_path)
            else:
                entry["metrics"] = evaluate_raster_standalone(agent_path)
            scores.append(entry["metrics"].get("score", 0))

        elif ext in ('shp', 'gpkg', 'geojson'):
            if gold_path and os.path.exists(gold_path):
                entry["metrics"] = evaluate_vector(gold_path, agent_path)
            else:
                entry["metrics"] = {"type": "vector", "has_gold": False, "score": 0.5 if file_size > 100 else 0.0}
            scores.append(entry["metrics"].get("score", 0))

        elif ext == 'csv':
            if gold_path and os.path.exists(gold_path):
                entry["metrics"] = evaluate_csv(gold_path, agent_path)
            else:
                entry["metrics"] = {"type": "csv", "has_gold": False, "score": 0.5 if file_size > 100 else 0.0}
            scores.append(entry["metrics"].get("score", 0))

        elif ext in ('dbf', 'shx', 'prj', 'cpg', 'npy'):
            entry["metrics"] = {"type": "auxiliary", "score": None}
            continue

        else:
            entry["metrics"] = {"type": "unknown", "score": 0.5 if file_size > 100 else 0.0}
            scores.append(entry["metrics"].get("score", 0))

        result["outputs"].append(entry)

    result["overall_score"] = round(float(np.mean(scores)), 3) if scores else 0.0
    result["task_success"] = result["overall_score"] > 0.3
    return result

