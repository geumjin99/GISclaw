def evaluate_csv(gold_path, agent_path):
    metrics = {"type": "csv"}
    try:
        g = pd.read_csv(gold_path)
        a = pd.read_csv(agent_path)
        metrics["gold_shape"] = list(g.shape)
        metrics["agent_shape"] = list(a.shape)
        metrics["shape_match"] = (g.shape == a.shape)
        common_cols = set(g.columns) & set(a.columns)
        metrics["column_overlap"] = len(common_cols) / max(len(g.columns), 1)

        num_cols = [c for c in common_cols if pd.api.types.is_numeric_dtype(g[c]) and pd.api.types.is_numeric_dtype(a[c])]
        if num_cols:
            corrs = []
            for c in num_cols[:10]:
                if len(g[c].dropna()) > 2 and len(a[c].dropna()) > 2:
                    min_len = min(len(g[c].dropna()), len(a[c].dropna()))
                    corr = np.corrcoef(g[c].dropna().values[:min_len], a[c].dropna().values[:min_len])[0, 1]
                    if not np.isnan(corr):
                        corrs.append(corr)
            if corrs:
                metrics["avg_column_correlation"] = round(float(np.mean(corrs)), 4)

        score = 0.0
        score += 0.3 * metrics.get("column_overlap", 0)
        if metrics.get("shape_match"): score += 0.2
        if metrics.get("avg_column_correlation", 0) > 0.8: score += 0.5
        elif metrics.get("avg_column_correlation", 0) > 0.5: score += 0.25
        metrics["score"] = round(score, 2)
    except Exception as e:
        metrics["error"] = str(e)
        metrics["score"] = 0.0
    return metrics

