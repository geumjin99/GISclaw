import concurrent.futures
import time

def process_task_model(arch_name, tid, task_label, instruction, gold_dir, label, model_dir, eval_json_path, skip_vision, force):
    if os.path.exists(eval_json_path) and not force:
        with open(eval_json_path) as f:
            existing = json.load(f)
        existing["arch"] = arch_name
        return existing
    
    if not os.path.isdir(model_dir):
        return None
        
    for _ in range(4): # retry loop
        try:
            result = evaluate_single(tid, label, model_dir, gold_dir, instruction, skip_vision=skip_vision)
            result["arch"] = arch_name
            with open(eval_json_path, 'w') as f:
                json.dump(result, f, indent=2, ensure_ascii=False)
            print(f"[{task_label}] {arch_name} {label}: [ok] score={result['overall_score']:.2f}")
            return result
        except Exception as e:
            if "rate" in str(e).lower() or "429" in str(e):
                print(f"[{task_label}] {arch_name} {label}: [wait] Rate limit, 15 ...")
                time.sleep(15)
            else:
                print(f"[{task_label}] {arch_name} {label}: [fail] Error: {e}")
                return None
    return None

def main():
    parser = argparse.ArgumentParser(description="")
    parser.add_argument("--arch", choices=["da", "sa", "both"], default="both")
    parser.add_argument("--skip-vision", action="store_true", help=" GPT-4o vision ")
    parser.add_argument("--force", action="store_true", help="")
    parser.add_argument("--tasks", type=str, default="", help=" task ID")
    args = parser.parse_args()

    tasks = [int(t) for t in args.tasks.split(',')] if args.tasks else list(range(1, 50))
    archs = []
    if args.arch in ("da", "both"):
        archs.append(("DA", DA_DIR, DA_MODELS))
    if args.arch in ("sa", "both"):
        archs.append(("SA", SA_DIR, SA_MODELS))

    print("=" * 60)
    print(f" | Tasks: {len(tasks)} | Vision: {'OFF' if args.skip_vision else 'ON'}")
    print("=" * 60)

    all_results = []
    tasks_to_run = []

    for arch_name, results_dir, model_map in archs:
        for tid in tasks:
            task_label = f"T{tid:02d}"
            instruction = load_task_instruction(tid)
            gold_dir = os.path.join(GOLD_DIR, task_label, "gold")
            for mk, mi in model_map.items():
                label = mi["label"]
                model_dir = os.path.join(results_dir, task_label, mi["path"])
                eval_json_path = os.path.join(model_dir, "eval_output.json")
                tasks_to_run.append((arch_name, tid, task_label, instruction, gold_dir, label, model_dir, eval_json_path, args.skip_vision, args.force))

    with concurrent.futures.ThreadPoolExecutor(max_workers=10) as executor:
        futures = [executor.submit(process_task_model, *targs) for targs in tasks_to_run]
        for future in concurrent.futures.as_completed(futures):
            res = future.result()
            if res:
                all_results.append(res)

    # Aggregate
    suffix = f"_{CELL_LABEL}" if CELL_LABEL else ""
    summary_path = os.path.join(OUTPUT_DIR, f"evaluation_summary{suffix}.json")
    summary = {
        "eval_time": datetime.now().isoformat(),
        "total_evaluated": len(all_results),
        "results": all_results,
    }
    with open(summary_path, 'w') as f:
        json.dump(summary, f, indent=2, ensure_ascii=False)
    print(f"\n[stats] Aggregate: {summary_path}")

    if all_results:
        print("\n" + "=" * 60)
        print(" ()")
        print("=" * 60)
        df = pd.DataFrame([{"arch": r["arch"], "model": r["model"], "score": r["overall_score"],
                            "success": r["task_success"]} for r in all_results])
        summary_df = df.groupby(["arch", "model"]).agg(
            avg_score=("score", "mean"),
            success_rate=("success", "mean"),
            count=("score", "count")
        ).round(3)
