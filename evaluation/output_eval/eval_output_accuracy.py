def evaluate_raster(gold_path, agent_path):
    """"""
    import rasterio
    metrics = {"type": "raster"}
    try:
        with rasterio.open(gold_path) as g, rasterio.open(agent_path) as a:
            g_data = g.read(1).astype(float)
            a_data = a.read(1).astype(float)

            metrics["shape_match"] = (g.shape == a.shape)
            metrics["crs_match"] = (str(g.crs) == str(a.crs))
            metrics["gold_shape"] = list(g.shape)
            metrics["agent_shape"] = list(a.shape)
            metrics["gold_crs"] = str(g.crs)
            metrics["agent_crs"] = str(a.crs)

            g_valid = g_data[~np.isnan(g_data)]
            a_valid = a_data[~np.isnan(a_data)]

            if len(g_valid) > 0 and len(a_valid) > 0:
                metrics["gold_stats"] = {"min": float(np.min(g_valid)), "max": float(np.max(g_valid)),
                                         "mean": float(np.mean(g_valid)), "std": float(np.std(g_valid))}
                metrics["agent_stats"] = {"min": float(np.min(a_valid)), "max": float(np.max(a_valid)),
                                          "mean": float(np.mean(a_valid)), "std": float(np.std(a_valid))}

                if metrics["gold_stats"]["mean"] != 0:
                    metrics["mean_relative_error"] = abs(metrics["gold_stats"]["mean"] - metrics["agent_stats"]["mean"]) / abs(metrics["gold_stats"]["mean"])

                if g.shape == a.shape:
                    valid_mask = ~np.isnan(g_data) & ~np.isnan(a_data)
                    if np.sum(valid_mask) > 10:
                        corr = np.corrcoef(g_data[valid_mask].flat, a_data[valid_mask].flat)[0, 1]
                        metrics["pixel_correlation"] = round(float(corr), 4) if not np.isnan(corr) else 0.0

            # (0-1)
            score = 0.0
            if metrics.get("shape_match"): score += 0.2
            if metrics.get("crs_match"): score += 0.2
            if metrics.get("pixel_correlation", 0) > 0.8: score += 0.3
            elif metrics.get("pixel_correlation", 0) > 0.5: score += 0.15
            if metrics.get("mean_relative_error", 1) < 0.1: score += 0.3
            elif metrics.get("mean_relative_error", 1) < 0.3: score += 0.15
            metrics["score"] = round(score, 2)

    except Exception as e:
        metrics["error"] = str(e)
        metrics["score"] = 0.0
    return metrics

def evaluate_raster_standalone(agent_path):
    """ gold agent """
    import rasterio
    metrics = {"type": "raster", "has_gold": False}
    try:
        with rasterio.open(agent_path) as a:
            a_data = a.read(1).astype(float)
            a_valid = a_data[~np.isnan(a_data)]
            metrics["agent_shape"] = list(a.shape)
            metrics["agent_crs"] = str(a.crs)
            if len(a_valid) > 0:
                metrics["agent_stats"] = {"min": float(np.min(a_valid)), "max": float(np.max(a_valid)),
                                          "mean": float(np.mean(a_valid)), "std": float(np.std(a_valid))}
                metrics["score"] = 0.5
            else:
                metrics["score"] = 0.1
    except Exception as e:
        metrics["error"] = str(e)
        metrics["score"] = 0.0
    return metrics

