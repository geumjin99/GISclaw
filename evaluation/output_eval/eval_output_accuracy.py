def evaluate_vector(gold_path, agent_path):
    """"""
    import geopandas as gpd
    metrics = {"type": "vector"}
    try:
        g = gpd.read_file(gold_path)
        a = gpd.read_file(agent_path)
        metrics["gold_features"] = len(g)
        metrics["agent_features"] = len(a)
        metrics["feature_count_match"] = (len(g) == len(a))
        metrics["crs_match"] = (str(g.crs) == str(a.crs))
        metrics["gold_columns"] = sorted(g.columns.tolist())
        metrics["agent_columns"] = sorted(a.columns.tolist())
        common_cols = set(g.columns) & set(a.columns) - {'geometry'}
        metrics["common_columns"] = len(common_cols)
        metrics["column_overlap"] = len(common_cols) / max(len(set(g.columns) - {'geometry'}), 1)

        score = 0.0
        if metrics["crs_match"]: score += 0.25
        if metrics["feature_count_match"]: score += 0.25
        score += 0.5 * metrics.get("column_overlap", 0)
        metrics["score"] = round(score, 2)
    except Exception as e:
        metrics["error"] = str(e)
        metrics["score"] = 0.0
    return metrics

